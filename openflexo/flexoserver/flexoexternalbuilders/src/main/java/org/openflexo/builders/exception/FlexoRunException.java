package org.openflexo.builders.exception;

public class FlexoRunException extends Exception{

	public FlexoRunException() {
		super();
	}

	public FlexoRunException(String message, Throwable cause) {
		super(message, cause);
	}

	public FlexoRunException(String message) {
		super(message);
	}

	public FlexoRunException(Throwable cause) {
		super(cause);
	}

	
}
